// ── Helpers ─────────────────────────────────────────────
export function getPlaceById(id) {
  return places.find(p => p.id === Number(id));
}

// Parse static distance string to meters (for sorting)
export function parseDistanceMeters(distStr) {
  if (!distStr) return Infinity;
  const num = parseFloat(distStr);
  if (distStr.includes('km')) return num * 1000;
  return num;
}

export function filterPlaces({ category, subcategory, query }) {
  return places.filter(p => {
    if (category && p.category !== category) return false;
    if (subcategory && subcategory !== '전체') {
      if (p.subCategory !== subcategory) return false;
    }
    if (query) {
      const q = query.toLowerCase();
      const inName = p.name?.toLowerCase().includes(q)
        || p.nameKo?.toLowerCase().includes(q)
        || p.nameEn?.toLowerCase().includes(q);
      const inAddr = p.address?.cn?.toLowerCase().includes(q)
        || p.address?.ko?.toLowerCase().includes(q);
      const inDesc = p.description?.ko?.toLowerCase().includes(q);
      if (!inName && !inAddr && !inDesc) return false;
    }
    return true;
  });
}
